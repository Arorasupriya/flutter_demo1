void callSimpleInterestGetData() {
  stdout.write("Enter Principle Amount : ");
  int pAmount = getIntegerValue();
  stdout.write("Enter Time/Years ");
  int time = getIntegerValue();
  stdout.write("Enter rate of interest : ");
  double rate = getIntegerValue().toDouble();
  var result = getSimpleInterest(pAmount, time, rate);
  print("Simple Interest is : $result\n");
}

double getSimpleInterest(int principleAmount, int time, double rate) {
  var result = (principleAmount * rate * time) / 100;
  return result;
}

