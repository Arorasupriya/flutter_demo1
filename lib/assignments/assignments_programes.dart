void letterIsVC(String letter) {
  if (letter == 'a' ||
      letter == 'A' ||
      letter == 'e' ||
      letter == 'E' ||
      letter == 'i' ||
      letter == 'I' ||
      letter == 'o' ||
      letter == 'O' ||
      letter == 'u' ||
      letter == 'U') {
    print("$letter is Vowel");
  } else {
    print("$letter is Consonant");
  }
}

